namespace VSTA.Core;

/// <summary>
/// Represents a word and its frequency
/// </summary>
public class WordFrequency
{
    /// <summary>
    /// The word.
    /// </summary>
    /// <returns>The word as a string.</returns>
    public string word { get; set; }
    
    /// <summary>
    /// The frequency.
    /// </summary>
    /// <returns>The frequency as a long.</returns>
    public long frequency  { get; set; }

}
